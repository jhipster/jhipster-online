import { Component, OnInit, OnDestroy } from '@angular/core';
import { ActivatedRoute } from '@angular/router';

import { NgbActiveModal, NgbModalRef } from '@ng-bootstrap/ng-bootstrap';
import { JhiEventManager } from 'ng-jhipster';

import { UserModalService } from './user-modal.service';
import { UserService } from 'app/core/user/user.service';
import { User } from 'app/core/user/user.model';

@Component({
  selector: 'jhi-user-mgmt-dialog',
  templateUrl: './user-management-dialog.component.html'
})
export class UserMgmtDialogComponent implements OnInit {
  user!: User;
  languages?: any[];
  authorities?: any[];
  isSaving?: Boolean;

  constructor(public activeModal: NgbActiveModal, private userService: UserService, private eventManager: JhiEventManager) {}

  ngOnInit(): void {
    this.isSaving = false;
    this.authorities = [];
    this.userService.authorities().subscribe((authorities: any) => {
      this.authorities = authorities;
    });
  }

  clear(): void {
    this.activeModal.dismiss('cancel');
  }

  save(): void {
    this.isSaving = true;
    if (this.user.id !== null) {
      this.userService.update(this.user).subscribe(
        (response: any) => this.onSaveSuccess(response),
        () => this.onSaveError()
      );
    } else {
      this.user.langKey = 'en';
      this.userService.create(this.user).subscribe(
        (response: any) => this.onSaveSuccess(response),
        () => this.onSaveError()
      );
    }
  }

  private onSaveSuccess(result: any): void {
    this.eventManager.broadcast({ name: 'userListModification', content: 'OK' });
    this.isSaving = false;
    this.activeModal.dismiss(result);
  }

  private onSaveError(): void {
    this.isSaving = false;
  }
}

@Component({
  selector: 'jhi-user-dialog',
  template: ''
})
export class UserDialogComponent implements OnInit, OnDestroy {
  modalRef?: NgbModalRef;
  routeSub: any;

  constructor(private route: ActivatedRoute, private userModalService: UserModalService) {}

  ngOnInit(): void {
    this.routeSub = this.route.params.subscribe(params => {
      if (params['login']) {
        this.modalRef = this.userModalService.open(UserMgmtDialogComponent as Component, params['login']);
      } else {
        this.modalRef = this.userModalService.open(UserMgmtDialogComponent as Component);
      }
    });
  }

  ngOnDestroy(): void {
    this.routeSub.unsubscribe();
  }
}
